// 3 - Utilize if/else para escrever um código que retorne o maior de três números. Defina, no começo do seu código, três variáveis com os valores que serão comparados.

let x = 87;
let y = 99;
let z = 111;

if (x > y && x > z){
    console.log(x);
} else if (y > x && y > z){
    console.log(y);
} else if (z > x && z > y){
    console.log(z);
}